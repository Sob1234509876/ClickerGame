package top.sob.core.gt;

public class Item {

    /** The gt`s name */
    protected String name;
    /** The gt`s description */
    protected String description;

    /**
     * Returns the name of the gt.
     * 
     * @return the name of the gt
     */
    public String getName() {
        return name;
    }

    /**
     * Returns the description of the gt.
     * @return the description of the gt
     */
    public String getDescription() {
        return description;
    }
}
